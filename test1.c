#include <stdio.h>
#include <stdlib.h>

int CheckDate(int* p){

	int tmp[7] = { 0 };

	int i;
	for (i = 0; i < 5; i++){
		// 遍历，看是否有重复，有重复返回0，如果发现没有重复的，就返回1
		if (tmp[p[i]]){
			return 0;
		}
		tmp[p[i]] = 1;
	}
	return 1;
}

